    $("#mapa rect").on("click", function () {
        /* Do pomocné proměnné id si uložíme id z atributu tagu <rect> */
        let Id = $(this).attr("id");
        /* Do proměnné un (znázorňuje unESCO) pro si uložíme vyhledaný výraz z id, který se porovná s JSON daty a našeho Id */
        let un = unesco.find(function (item) {
            return item.id == Id;
        });
        /* Vyčistí se nám oblast pro zobrazování obrázků u památek UNESCO */
        $("#karty").empty();
        /* Zde dojde k zobrazení informací o jednotlivé památce UNESCO. */
        /* Já jsem uvedl pouze 11 "nejznámějších" památek, je jichc samozřejmě více - můžou se kdykoliv přidat */
        $("#odstavec").fadeOut(200, function () {
            /* Pomocí metody html vepíšeme informace do tagu <div> s id #odstavec */
            $("#odstavec").html(`
          <h4 class="bg-warning p-2 mt-2">${un.title}</h4><span class="badge badge-primary">
          ${un.income}</span>
          <p class="blok">${un.description}</p>`).slideDown(800);
          /* Cyklus for pro vykreslení obrázků z JSON, kde je u každého objektu pole s odkazy na obrázky */
            for (i = 0; i < un.img.length; i++) {
                /* Metoda append mi zajistí změnu obrázků a výpis na konec. Z tohoto důvodu používám i metodu empty() */
                $("#karty").append(`
                <img src="${un.img[i]}" alt="${un.title}" height="300">`);
            }
        });
    });
