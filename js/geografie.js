    $("#check1").click(function () {
        if ($(this).prop("checked") == true)
            $("g circle").show(400);
        else if ($(this).prop("checked") == false)
            $("g circle").hide(400);
    });
    /* Pomocná proměnná pro určení barvy, která se mění, když uživatel přejíždí po mapě */
    let barva = '';
    $("#mapa path").on("mouseover", function () {
        barva = $(this).css("fill");
        $(this).css('fill', '#ffc107');
        /* Efekt skrytí a odkrytí */
        $(this).fadeOut().fadeIn();
    });
    /* Když myš opustí oblast z předchozích řádků, tzn. mouseover, přebarví se zpět na původní barvu (proměnná barva) */
    $("#mapa path").on("mouseout", function () {
        $(this).css('fill', barva);
    });
    /* Zobrazování jednotlivých spolkových zemí do nadpisu.
     Zde právě využijeme ony clicks, na základě nichž dojde k odkrývání názvů zemí. Doporučuji si pustit ukázku.*/
    $("#mapa path").on("click", function () {
        let atribut = $(this).attr("name");
        clicks++;
        if (clicks == 0) {
            $("#zeme").show(500).text(atribut);
        }
        else
            $("#zeme").hide().show(500).text(atribut);
    });
    /* Pokud uživateli vadí zobrazená lištička s názvem země, jednoduše na ni klikne a je pryč. */
