$(function () {
    $("h4").on("click", function () {
        $(this).next().toggle(1500);
        $(".card-title").next().empty();
    });
    $(".toglik").on("click", function(){
        $(this).parents(".row").next().toggle(1500);
    })
    udalosti.forEach((item) => {
        $("#udalosti").append(`<div class="row border p-3 bg-dark text-warning m-0 fontik"><div class="col-md-4 border-right"><p>${item.year}</p></div><div class="col-md-6 border-right title"><p><i class="fas fa-chevron-down"></i> ${item.title}</p><p class="des">${item.description}</p></div><div class="col-md-2"><p>${(item.destruction == "false") ? '<i class="fas fa-check text-warning"></i>' : '<i class="fas fa-times"></i>'}</p></div></div>`);
        $(".des").hide();
        $(".title").on("click", function () {
            $(".des").hide();
            $(this).children().show(300);
        });
    });
    osobnosti.forEach((osobnost) => {
        $("#karty .row").append(`<div class="card ml-5 mb-3 border-warning" style="width: 18rem;"><img class="card-img-top" src="${osobnost.image}" alt="Osobnost" height="300"><div class="card-body"><h5 class="card-title">${osobnost.name}</h5><div class="information"></div></div>`);
    });
    $(".card-title").on("click", function(){
        $(this).next().empty();
        let jmeno = $(this).text();
        let osobnost = osobnosti.find(item => {return item.name === jmeno});
        $(this).next().append(`<table class='table table-bordered'><tbody><tr><td class='font-weight-bold'>Datum narození</td><td>${osobnost.born}</td></tr><tr><td class='font-weight-bold'>Datum úmrtí</td><td>${osobnost.died}</td></tr></tbody></table><p class='blok'>${osobnost.description}<p>`).hide().show(2000);
    });
});
